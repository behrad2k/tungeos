#include <stdarg.h>

void puts(const char *string) {
	x+=strlen(string);
	update_cursor(x,y);
	for (int i = 0; i < strlen(string); i++) {
		if (string[i] == '\n') {
			terminal_row++;
			y++;

			update_cursor(x,y);
		} else {
			terminal_putchar(string[i]);
		}
	}
}

void putc(const char string) {
	if (string == '\n') {
		terminal_row++;
		y++;
		update_cursor(x, y);
	} else if (string == '\t') {
		terminal_putchar(' ');
		terminal_putchar(' ');
		terminal_putchar(' ');
		terminal_putchar(' ');
	} else {
		terminal_putchar(string);
	}
}